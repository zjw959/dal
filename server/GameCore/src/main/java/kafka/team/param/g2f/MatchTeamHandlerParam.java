package kafka.team.param.g2f;

public class MatchTeamHandlerParam {

    public int playerId;

    public String name;

    public int level;

    public int HeroCid;

    public int skinCid;

    public int gameServerId;

    public int dungeonCid;

    public int getPlayerId() {
        return playerId;
    }

    public void setPlayerId(int playerId) {
        this.playerId = playerId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getLevel() {
        return level;
    }

    public void setLevel(int level) {
        this.level = level;
    }

    public int getHeroCid() {
        return HeroCid;
    }

    public void setHeroCid(int heroCid) {
        HeroCid = heroCid;
    }

    public int getSkinCid() {
        return skinCid;
    }

    public void setSkinCid(int skinCid) {
        this.skinCid = skinCid;
    }

    public int getGameServerId() {
        return gameServerId;
    }

    public void setGameServerId(int gameServerId) {
        this.gameServerId = gameServerId;
    }

    public int getDungeonCid() {
        return dungeonCid;
    }

    public void setDungeonCid(int dungeonCid) {
        this.dungeonCid = dungeonCid;
    }


}
